

USE ROLE DEVELOPER ;
USE WAREHOUSE PROJECT ;

--- LIST FILES 

LIST @&DB_NAME.BRONZE.RAW;

---- ingest csv


COPY INTO &DB_NAME.BRONZE.CSV_RAW_CONTENT
FROM (
    SELECT 
        $1 as raw_line,
        METADATA$FILENAME AS FILE_NAME,
        METADATA$FILE_ROW_NUMBER,
        '&{BATCH_ID}'
    FROM @&DB_NAME.BRONZE.RAW
    )
FILE_FORMAT = '&DB_NAME.BRONZE.CSV'
PATTERN = '(?i).*\.csv.*';

--- Json and XML are loaded in text plane
------------- JSON

COPY INTO &DB_NAME.BRONZE.RAW_JSON_INGESTION 
FROM (
    SELECT $1,
    METADATA$FILENAME AS FILE_NAME,
    METADATA$FILE_ROW_NUMBER,
    CURRENT_TIMESTAMP(),
    '&{BATCH_ID}'
    FROM @&DB_NAME.BRONZE.RAW
)
FILE_FORMAT = (FORMAT_NAME = '&DB_NAME.BRONZE.SEMITRUCTURED_DATA')
PATTERN = '.*\.json.*'
ON_ERROR = CONTINUE;


--- XML --------------------------

COPY INTO &DB_NAME.BRONZE.RAW_XML_INGESTION 
FROM (
    SELECT $1, 
    METADATA$FILENAME, 
    METADATA$File_row_number, 
    CURRENT_TIMESTAMP(),
    '&{BATCH_ID}'
    FROM @&DB_NAME.BRONZE.RAW
)
FILE_FORMAT = (FORMAT_NAME = '&DB_NAME.BRONZE.SEMITRUCTURED_DATA')
PATTERN = '.*\.(xml|txt).*'
ON_ERROR = CONTINUE;


